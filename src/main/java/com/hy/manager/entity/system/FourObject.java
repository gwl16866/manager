package com.hy.manager.entity.system;

import java.util.ArrayList;

public class FourObject {


    private ArrayList<String> peoples;
    private ArrayList<String> message;
    private ArrayList<String> money;
    private ArrayList<String> shopping;

    public ArrayList<String> getPeoples() {
        return peoples;
    }

    public void setPeoples(ArrayList<String> peoples) {
        this.peoples = peoples;
    }

    public ArrayList<String> getMessage() {
        return message;
    }

    public void setMessage(ArrayList<String> message) {
        this.message = message;
    }

    public ArrayList<String> getMoney() {
        return money;
    }

    public void setMoney(ArrayList<String> money) {
        this.money = money;
    }

    public ArrayList<String> getShopping() {
        return shopping;
    }

    public void setShopping(ArrayList<String> shopping) {
        this.shopping = shopping;
    }
}
